public class TaskCheckDemo {

    public static void main(String[] args) {
        // Initialize the integer variable
        int value = 180;

        // Display the initial value
        System.out.println("Initial value: " + value);

        // Pre-increment (value++) and display the result
        System.out.println("After post-increment (value++): " + (value++));

        // Pre-decrement (value--) and display the result
        System.out.println("After post-decrement (value--): " + (value--));
    }

}